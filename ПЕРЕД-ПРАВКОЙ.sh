#!/bin/bash
# Сохранить снимок перед большой правкой.
# Использование:
#   bash ПЕРЕД-ПРАВКОЙ.sh "реферальная система"
#   bash ПЕРЕД-ПРАВКОЙ.sh "оплата Stars"
#   bash ПЕРЕД-ПРАВКОЙ.sh "новые уроки"
#
# Если забудешь параметр — скрипт попросит ввести.
cd "$(dirname "$0")"

if [ -z "$1" ]; then
  echo "Использование:"
  echo "  bash ПЕРЕД-ПРАВКОЙ.sh \"название большого изменения\""
  exit 1
fi

NAME="$1"
git add -A
git commit -m "🔵 Снимок перед: $NAME" 2>&1 | tail -1
echo ""
echo "✅ Снимок перед «$NAME» сделан! Текущая история:"
git log --oneline -8
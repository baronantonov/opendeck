#!/bin/bash
# Сохранить текущую рабочую версию (снимок).
# Запуск: двойной клик или "bash СОХРАНИТЬ-ВЕРСИЮ.sh"
cd "$(dirname "$0")"
git add -A
git commit -m "Рабочая версия от $(date '+%d.%m.%Y %H:%M')" 2>&1 | tail -1
echo ""
echo "✅ Версия сохранена! Список всех сохранённых версий:"
git log --oneline -10
